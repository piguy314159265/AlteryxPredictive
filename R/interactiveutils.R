#' Create a dashboard outputting a single message
#'
#' @param messageIn message to output
#' @param titleIn title of dash
#' @param colorIn color in dash
#' @param widthIn width
#' @return dashboard object for rendering
#' @author Todd Morley
badDash <- function(
  messageIn,
  titleIn = "Error",
  colorIn = 'aqua',
  widthIn = 12
){
  requireNamespace("flightdeck")
  header <- fdHeader(
    title = titleIn,
    titleWidth = 600)
  row <- fdRow(
    fdBox(
      fdInfoBox(
        title = NULL,
        value = messageIn,
        subtitle = NULL,
        icon = fdIcon('flash', 'entypo'),
        color = colorIn,
        width = widthIn
      ),
      width = widthIn
    )
  )
  page <- fdPage(
    row,
    id = 'page',
    display = TRUE
  )
  body <- fdBody(
    page
  )
  sidebar <- fdSidebar()
  fdBoard(
    header = header,
    sidebar = NULL,
    body = body,
    fixed = TRUE
  )
}

#' Writes default error dashboard
#'
#' @param titleIn title of dash
#' @param colorIn color in dash
#' @param widthIn width
#' @return dashboard object for rendering
#' @export
errorDash <- function(
  titleIn = "Error",
  colorIn = "aqua",
  widthIn = 12
){
  return(
    badDash(
      "Error in generating interactive report but not in model building."
    )
  )
}
